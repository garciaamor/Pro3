public void respuestasAleatorias(String correcta,String falsa1, String falsa2){
    
    int posicion=(int)Math.floor(Math.random() * 3)+1;
    switch(posicion){
        case 1: A=correcta;
                B=falsa1;
                C=falsa2;
                
                break;
        case 2: A=falsa1;
                B=correcta;
                C=falsa2;
            break;
        case 3:  A=falsa2;
                 B=falsa1;
                 C=correcta;
            break;
    }
                
    lRespuestaA.setText("a)"+A);
    
    lRespuestaB.setText("b)"+B);
    
    lRespuestaC.setText("c)"+C);
}
